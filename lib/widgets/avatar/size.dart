/// Defines size of a avatar.
enum UFUAvatarSize {
  /// Defines large size [UFUAvatarSize.large] of width = height = 128.0 of a avatar.
  extraLarge,

  /// Defines large size [UFUAvatarSize.large] of width = height = 42.0 of a avatar.
  large,

  /// Defines large size [UFUAvatarSize.medium] of width = height = 36.0 of a avatar.
  medium,

  /// Defines large size [UFUAvatarSize.small] of width = height = 24.0 of a avatar.
  small,

  /// Defines large size [UFUAvatarSize.size_72x72] of width = height = 72.0 of a avatar.
  size_72x72,

  /// Defines large size [UFUAvatarSize.size_30x30] of width = height = 30.0 of a avatar.
  size_30x30,
}
