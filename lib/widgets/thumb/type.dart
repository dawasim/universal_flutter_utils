/// [UFUThumbType] is used to set type of a thumb widget
enum UFUThumbType {
  /// Default type is [UFUThumbType.folder] is used to set folder type of a thumb widget
  folder,

  ///[UFUThumbType.image] is used to set folder type of a thumb widget
  image,

  ///[UFUThumbType.icon] is used to set folder type of a thumb widget
  icon,
}
