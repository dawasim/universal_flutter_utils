/// [UFUButtonColorType] is used to change background color, border color button widget.
enum UFUButtonColorType {
  /// Default color type is [UFUButtonColorType.primary], used for set primary color
  primary,

  /// [UFUButtonColorType.tertiary], used for set tertiary color
  tertiary,

  /// [UFUButtonColorType.lightGray], used for set light gray color
  lightGray,

  /// [UFUButtonColorType.lightBlue], used for set light blue color
  lightBlue,

  /// [UFUButtonColorType.secondary], used for set secondary color
  secondary,

  /// [UFUButtonColorType.secondaryLight], used for set secondary color
  secondaryLight,

  /// [UFUButtonColorType.transparent], used for set transparent color
  transparent,

  /// [UFUButtonColorType.base], used for set base color
  base,
}
